// Write a program that takes two integers as input and performs 
//addition, subtraction, multiplication, and division, then prints the results.

package main
import "fmt"
func main() {
	num1, num2 := 80, 40
	fmt.Println("Num 1 :",num1,", Num 2 :",num2)
	fmt.Println("Additon : ",num1+num2)
	fmt.Println("Substraction : ",num1-num2)
	fmt.Println("Multiplication : ",num1*num2)
	fmt.Println("Division : ",num1/num2)
}
